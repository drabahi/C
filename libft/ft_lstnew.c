#include "libft.h"
#include <stdlib.h>
#include <string.h>

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list		*link;

	if ((link = malloc(sizeof(t_list))) == NULL)
		return (NULL);
	if (content == NULL)
	{
		link->content = NULL;
		link->content_size = 0;
	}
	else
	{
		link->content = (void *)content;
		link->content_size = content_size;
	}
	return (link);
}
