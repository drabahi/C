#include "libft.h"
#include <stdlib.h>

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*d;
	int		i;

	if (f == NULL || s == NULL)
		return (NULL);
	d = (char *)malloc(sizeof(*d) * (ft_strlen(s) + 1));
	i = 0;
	while (s[i])
	{
		d[i] = f(s[i]);
		i++;
	}
	d[i] = '\0';
	return (d);
}
