#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;
	int		diff;

	i = 0;
	diff = 0;
	if (!s1)
		s1 = "";
	if (!s2)
		s2 = "";
	while (!diff && (s1[i] != '\0' || s2[i] != '\0'))
	{
		diff = s1[i] - s2[i];
		i++;
	}
	return (diff);
}

